<template lang='haml'>
  .column.column-main-body
    .documentation-body
      %h2 Stores and Supplies
      %a#stores

    .documentation-body
      %h3 What is the point of building stores?
      %p
        Once you start your company off, you need to generate some sort of income. Probably the best and easiest place to
        invest your money would be with stores. People are always in need of food and other products.

    .documentation-body
      %h3 Can my company survive without stores?
      %p
        Absolutely. You can go into the real estate market and just stick with that. That ensures you'll have a safe and
        steady market. But if you're brave, and want to have more fun, you'll want to enter the commercial market and
        test your luck.

    .documentation-body
      %h3 Once I build stores, why don't they generate income?
      %p
        There are many possible answers to that question. It might be because the population isn't in need of what you're
        selling. It could be because there aren't too many residents around the store. Normally the store finds producers
        to buy from automatically, but they might be out of products to sell, so your store ends up empty. To modify that
        situation, you can go to the inspect page and connect the store to a factory or a warehouse that is offering to sell
        something you would like at the right price. If nothing works, it's possible that location just isn't right (low end
        store near high class dwellings).

    .documentation-body
      %h3 To what should I connect the stores?
      %p
        You can connect to any factory or warehouse available. To find them, you can search for them in any town you like,
        using the Spider menu or go directly to the suppliers page in the store's inspect panel. Just click on the Hire
        button on the suppliers page.

    .documentation-body
      %h3 Does that mean I have to go in the industrial business?
      %p
        No. You can buy from any factory you want. That goes for everybody. But if you want, you can go in the industrial
        business so that your stores can buy from your own factories. There are always pros and cons to every choice.

    .documentation-body
      %h3 Do I choose who I want as my supplier?
      %p
        Yes. If the price of one supplier does not please you, you can go choose another. Even the town that supplier
        is in may be a factor.

    .documentation-body
      %h3 Is it possible to have more than one supplier?
      %p
        Yes. But you have to be careful. For each supply your store needs, you can only connect to 20 suppliers. That's why
        connecting to warehouses is so important, since they can handle very large amounts of products and they don't have
        any limits on the number of connections.
