    %h3 STARPEACE Community Conduct
    %p
      As the STARPEACE Community is an extensions of the game, postings to these forums are bound by the
      STARPEACE website
      %a{href:'https://www.starpeace.io/privacy'} End User License Agreement
      . In addition to the terms of the agreement, we have a few supplemental rules that apply to the STARPEACE Community:
    %p
      You may post any material, written in a courteous and mature manner, providing that it is on-topic for the
      forum to which you are posting. This includes material that disagrees with the way that we, the developers,
      operate the game. We will not interfere with the communication of thoughts and ideas as long as the
      presentation is appropriate for all those capable of reading the forum.
    %p
      You may not, however, post any material that:
    %p.content
      %ul
        %li
          Attacks or insults others on the board. Feel free to debate the idea, but do not turn your disagreement
          into an attack upon the poster.
        %li
          Engages in name-calling, harassment, or threats. See above.
        %li
          Contains obscenity, vulgarity, or profanity.
        %li
          Disparages any religion, race, nation, gender, or sexual orientation.
        %li
          Is considered inappropriate for children 13 or over, as governed by general standards of decency.
        %li
          Infringes on anyone's privacy rights.
    %p
      Furthermore, you may not:
    %p.content
      %ul
        %li
          Make multiple posts on the same subject for the purpose of getting attention paid to your issue.
        %li
          Post off-topic comments in threads for the purpose of drawing attention to another thread on a
          different subject.
        %li
          Monopolize the front page of any forum by 'bumping' threads of the same or similar content to
          the front. We may intervene by closing threads any time that this occurs.
        %li
          Use the official forums as a staging-ground for creating general unrest within the game (e.g. Sit-ins).
        %li
          Use the official forums as a place to advertise the sale of STARPEACE accounts in exchange for
          real-world money.
        %li
          Advertise web-sites with content that violates these rules, or the STARPEACE EULA.
        %li
          Advertise products or websites that are not related to STARPEACE.

    %p
      Action that we might take in regards to a post in violation of these rules on the official STARPEACE
    %p
      Forums will vary depending upon the transgression. It could include any number of the following:
    %p.content
      %ul
        %li Editing of the post.
        %li Deletion of the post/thread.
        %li Closing of the thread.
        %li Suspension (results in user being unable to play STARPEACE for the duration of the suspension).
        %li
          Banishment (results in the user's account being canceled. User will no longer be able to access
          STARPEACE, or any STARPEACE Community).
        %li Legal Action (i.e. contacting law enforcement personnel in regard to a threat).
    %p
      Violators of the board's rules may or may not receive a warning before action is taken. In the event
      that a warning is given, the warning will be recorded in the game account and may result in further
      action should the player show a pattern of engaging in prohibited conduct. Philosophically and
      administratively, there is no distinction between conduct in-game, and conduct in the STARPEACE Community.

