    %h3 Fair Play Policies
    %p
      Players are also subject to these supplementary rules while playing STARPEACE. While by no means an
      all-inclusive list of the do's and don'ts of playing STARPEACE, the following provides a suitable
      foundation from which the player can determine what activities are appropriate

    %p.content
      %ol
        %li
          As a mayor, you may not abuse zoning. Specifically, it is forbidden to use your position as mayor
          to arbitrarily zone and rezone areas of the map to derive an advantage for a player or to damage
          the interests of some players.
        %li
          You may not abuse the game so as to entirely monopolize a particular business. While there are
          instances where a player may be the only one operating facilities in a particular business (auto
          dealerships, mines), overly aggressive tactics to drive or keep out competitors may be considered
          contrary to the spirit of the game and the rules of conduct.
        %li
          You must comply with arbitration in contested issues. When two or more players or groups have
          conflicting positions regarding their businesses and their interactions, they are required to
          compromise. If an equitable compromise cannot be reached between the players prior to STARPEACE
          Support's involvement, Support will mandate a binding compromise. Refusing to abide by a compromise
          mandated by Support will be considered a disruption. It is therefore strongly suggested that players
          make every attempt possible to reach a compromise that they can live with. Support should only be
          contacted as a last resort as the compromise they will mandate may not necessarily suit the parties
          to the extent that a player-devised solution would. Note: A 'group' in this case is defined as a
          party of one or more characters that are united in a common belief or goal.
        %li
          Again, foul language is not permitted, in any language. Excessive use of foul language in an
          inappropriate context, including swear words, real-world racial slurs, and other language that
          is not consistent with the STARPEACE environment and designed to hurt, will be considered a
          disruption. The existence of the 'Ignore' function is not a license to be profane.
        %li
          To expound upon the general guidelines, you may not harass other players. Harassment is defined
          as specifically targeting another player or group of players to harm or inconvenience them.
          Harassment can take many forms, as it goes to the state-of-mind of the person or party on the
          receiving end of the action. However, in order to account for those who are excessively
          thin-skinned, STARPEACE Support will make a determination as to whether or not the average
          person would feel 'harassed' and act accordingly.
        %li
          You may not disrupt the normal playability of a zone or area. Zone/Area Disruption is defined as
          any activity designed to harm or inconvenience a number of groups, rather than a specific player
          or group of players. This includes actions such as:
          %ul
            %li
              Creating garbage dumps or industrial facilities near residential areas.
            %li
              Excessive and unwarranted use of roads or other structures to occupy land.
            %li
              Refusing to cooperate with the other parties in a contested issue after having been instructed
              to do so by STARPEACE Support.
        %li
          You may not defraud other players. Fraud in all transactions between players will result in
          disciplinary action when confirmed by an internal audit of STARPEACE. Fraud is defined as
          falsely representing one's intentions to make a gain at another's expense. Examples of this
          activity include but are not limited to offering to give STARPEACE money in exchange for some
          game-related favors (e.g. I will give you 1 billion if you build ten Movie theaters in my town)
          and then refusing to comply without a valid reason.
        %li
          You may not abuse other players, customer service representatives, or the game system. Though some
          of these actions are covered in part by other rules, they deserve their own note here. The following
          would be considered an 'abuse' in-game:
          %ul
            %li
              Hate Mongering - participation in or propagation of hate literature, behavior, or propaganda
              related to real-world characteristics.
            %li
              Sexual Abuse or Harassment - untoward and unwelcome advances of a graphic and sexual nature.
              This includes virtual rape, overt sexual overtures, and stalking of a sexual nature.
            %li
              Sexual Abuse or Harassment - untoward and unwelcome advances of a graphic and sexual nature.
              This includes virtual rape, overt sexual overtures, and stalking of a sexual nature.
            %li
              Attempting to Defraud a Support Representative - petitioning with untrue information with the
              intention of receiving benefits as a result. This includes reporting false bugs, or accusing
              other players of wrongdoing without basis for it.
            %li
              Impersonating a Support Representative - falsely representing yourself as a Game Master.
            %li
              Support Personnel Abuse - sending excessive requests for a Game Master chat or unduly using
              other channels to communicate to a Support representative, making physical threats, or using
              abusive language against a Support representative.
            %li
              Using Threats of Retribution by Game Masters or Support Staff - attempting to convince another
              player that they have no recourse in a disagreement by alleging that Support staff favors one
              party over another.

