    %h3 Naming Policy
    %p
      These guidelines apply to all names. Account, company, building, and product names in STARPEACE should
      reflect the genre of the game.
    %p
      The following types of names are unacceptable and are listed in order from the worst to the least offensive:
    %p.content
      %ol
        %li
          Vile, profane, rude, or racist names including common swear words, anatomical references, racial slurs,
          and homonyms of these words.
        %li
          Words or combinations of words that produce an offensive result, or that promote racist or religious
          intolerance, homophobia, xenophobia, violence or war against any people, or hate of any group.
        %li
          Names of religious, occult, or significant historic origin (e.g. Jesus Christ).
        %li
          Copyrighted or trademarked names of products, characters, services, or concepts (e.g. Oldsmobile,
          Microsoft, Sony).
        %li
          Names from popular media. These names can be either fictional (e.g. Rambo, Darthvader) or
          non-fictional (e.g. Garth Brooks, Pierce Brosnan, Ronald Reagan, Michael Jordan).
        %li
          The names of Support representatives or STARPEACE project leaders.
        %li
          Names chosen with the intent or possessed with the effect of harming the reputation of a
          player or Support representative.
    %p
      For all of the above, misspellings and alternative spellings of the word or words are also unacceptable.
    %p
      If a name is found to be in violation of any of the above polices, a Support manager will then use the following process:
    %p.content
      %ul
        %li
          For category 1 or 2 offenses, a GM will delete the company or account as soon as it is discovered.
          You will not be notified of this deletion. The companies in the account will not be restored or
          reimbursed under any circumstances. Depending on the nature of the offensive name, the account may
          also be subject to disciplinary action including suspension or banning. A GM will not contact you
          before disciplinary action is taken.
        %li
          For categories 3 through 7, the GM will contact you either in-game or via email to notify you of
          the need for a name change. Please be aware that if you do not choose a name that is in accordance
          with this naming policy at the time of creation, a new name will be assigned to your account or
          company and this name will not be changed. A name change for a company or an account will not cause
          a reset for the company (or all companies under the account).
    %p
      %b Conditions for Name Changes:
      &nbsp;Names will not be changed unless they violate the Naming Policy. Players may ask to have their
      account or company name changed, or ask for a confirmation that a name does not violate these rules.

